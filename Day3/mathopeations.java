public class MathOperations {
    public static void main(String[] args) {
        double powerResult = Math.pow(5, 6);
        double sqrtResult = Math.sqrt(45);
        double logResult = Math.log(1);

        System.out.println("Power(5, 6): " + powerResult);
        System.out.println("Sqrt(45): " + sqrtResult);
        System.out.println("Log(1): " + logResult);
    }
}